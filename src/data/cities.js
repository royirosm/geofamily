// Helper: get city name in given language
export function getCityName(cityEn, lang) {
  if (lang === 'el') return CITY_NAMES_EL[cityEn] || cityEn
  return cityEn
}

// Helper: get all cities for a country
export function getCitiesForCountry(code) {
  return CITIES[code] || []
}

// Helper: get non-capital cities for find-city direction.
// Removes the last entry (which is the capital in our ordering convention)
// unless the country only has one city.
export function getNonCapitalCities(code, capitalEn) {
  const all = getCitiesForCountry(code)
  if (all.length <= 1) return all
  // Filter out the capital city (case-insensitive match)
  const normCap = capitalEn ? capitalEn.trim().toLowerCase() : ''
  const filtered = all.filter(c => c.trim().toLowerCase() !== normCap)
  return filtered.length > 0 ? filtered : all
}
